#define N(array) (sizeof(array) / sizeof(*array))

void* smalloc(size_t size)
{
	void* ptr = malloc(size);
	if (!ptr)
	{
		fprintf(stderr, "%s: malloc(%lu): %m\n", argv0, size);
		exit(1);
	}
	return ptr;
}

